export interface IVolunteer {
  id: string
  firstName: string
  lastName: string
  email: string
  phone: string
  status: 'active' | 'inactive' | 'pending'
  role: 'Tier 1' | 'Tier 2' | 'Teen' | 'Admin'
  joinDate: string
  bio?: string
  photoUrl?: string
  skills: string[]
  reliabilityScore: number // 0-100

  // Detailed Form Data
  address: string
  city: string
  zip: string
  birthday: string
  age?: number
  allergies: boolean
  emergencyContactName: string
  emergencyContactPhone: string
  volunteerExperience?: string
  interestReason?: string
  positionPreferences: string[]
  availability: string[] // "Monday Morning", "Tuesday Evening"

  // Gamification Metrics
  totalHours: number
  streak: number // consecutive weeks
  badges: string[] // "Early Bird", "Weekend Warrior"
}

export interface IShift {
  id: string
  volunteerId: string
  date: string // ISO date
  startTime: string // "09:00"
  endTime: string // "13:00"
  role: string // "Dog Walking", "Cleaning", etc.
  status: 'scheduled' | 'all_good' | 'late' | 'no_show' | 'cancelled'
  notes?: string
}

export interface IIncident {
  id: string
  volunteerId: string
  date: string // ISO date
  severity: 'low' | 'medium' | 'high'
  description: string
  recordedBy: string
}

